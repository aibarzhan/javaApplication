package lesson2;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        byte a = 0b1100; //Двойчная система исчисление
        byte b = 014; //Восмиричная система исчисление
        byte c = 0xC; //Шестнад-ная система исчисление
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        short d = 0b10100010100; //Двойчная система исчисление
        short e = 02424;  //Восмиричная система исчисление
        short f = 0x514;   //Шестнад-ная система исчисление
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);


    }
}
